     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // dd($request->all());
        $request->validate([
            'title' => ['required', 'string'],
        ]);
        return response()->json($request->only(['title', 'author', 'status', 'description']));
    }

